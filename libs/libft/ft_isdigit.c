/**
 * Checks if a character is a digit (0-9).
 *
 * @param[in] c The character to check.
 * @return Non-zero if a digit, zero if not.
 */
int	ft_isdigit(int c)
{
	return ((c >= '0' && c <= '9') * 2048);
}
