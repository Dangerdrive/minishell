#include "libft.h"

/**
 * Maps each element of a linked list using a function and creates a new list.
 *
 * @param[in] lst The original list to be mapped.
 * @param[in] f The function to apply to each element's content.
 * @param[in] del The function to free content in case of failure.
 * @return A new linked list or NULL if allocation fails.
 */
t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new_node;

	if (!lst || !f)
		return (NULL);
	new_list = NULL;
	while (lst)
	{
		new_node = ft_lstnew(f(lst->content));
		if (!new_node)
		{
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new_node);
		lst = lst->next;
	}
	return (new_list);
}
